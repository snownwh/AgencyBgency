	REG_DATE DATE NOT NULL,
	STAR NUMBER(8) NOT NULL,
	REVIEW VARCHAR2(4000) NOT NULL,
	LIKE_NUMBER NUMBER(8),
	DISLIKE NUMBER(8),
	DEL NUMBER(1)
